import { mkdir, readFile, writeFile } from "node:fs/promises";
import { dirname } from "node:path";
import { pathToFileURL } from "node:url";
import esbuild from "esbuild";

const DEFAULT_OUTFILE = "src/generated/admin-ui.ts";

export async function buildAdminUi(options = {}) {
	const outfile = options.outfile || DEFAULT_OUTFILE;
	const result = await esbuild.build({
		bundle: true,
		entryPoints: ["src/admin-ui/main.tsx"],
		entryNames: "[name]",
		format: "iife",
		globalName: "GeminiAccountAdminUi",
		jsx: "automatic",
		jsxImportSource: "preact",
		legalComments: "none",
		logLevel: "silent",
		minify: true,
		outdir: "admin-ui",
		platform: "browser",
		target: "es2025",
		write: false,
	});

	const js = result.outputFiles.find((file) => file.path.endsWith(".js"))?.text;
	if (!js) {
		throw new Error("admin UI JavaScript bundle was not emitted");
	}

	const css = result.outputFiles.find((file) =>
		file.path.endsWith(".css"),
	)?.text;
	if (!css) {
		throw new Error("admin UI CSS bundle was not emitted");
	}

	const html = `<!doctype html>
<html lang="en" data-theme="light">
<head>
  <meta charset="utf-8">
  <meta name="viewport" content="width=device-width, initial-scale=1">
  <title>Gemini Account Pool</title>
  <style>${css}</style>
</head>
<body>
  <div id="app"></div>
  <script>${js}</script>
</body>
</html>`;

	const source = [
		"// Generated by scripts/build-admin-ui.mjs. Do not edit by hand.",
		`export const GEMINI_ACCOUNT_ADMIN_HTML = ${JSON.stringify(html)};`,
		"",
	].join("\n");

	return {
		outfile,
		written: await writeIfChanged(outfile, source),
	};
}

async function writeIfChanged(filePath, content) {
	try {
		if ((await readFile(filePath, "utf8")) === content) return false;
	} catch (error) {
		if (error?.code !== "ENOENT") throw error;
	}

	await mkdir(dirname(filePath), { recursive: true });
	await writeFile(filePath, content);
	return true;
}

if (
	process.argv[1] &&
	import.meta.url === pathToFileURL(process.argv[1]).href
) {
	await buildAdminUi();
}
